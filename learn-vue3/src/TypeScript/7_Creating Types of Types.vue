      조건부 타입은 입력 타입에 따라 다른 타입을 반환할 수 있다.
      이는 타입의 조건 분기를 가능하게 하여 더 동적인 타입 연산을 제공한다.

    6. Mapped Types - 기존 타입의 각 속성을 매핑하여 타입 생성
      매핑된 타입은 기존 타입의 각 키를 순회하면서 변형을 가할 수 있는 방법을 제공한다.
      이를 통해 유현하게 타입을 변형시킬 수 있다.

    7. Template Literal Types - 템플릿 리터럴 문자열을 통해 속성을 변경하는 매핑 타입
      템플릿 리터럴 타입은 문자열 템플릿을 사용하여 타입을 동적으로 생성할 수 있다.
