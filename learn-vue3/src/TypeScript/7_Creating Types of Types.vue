    이미 가지고 있는 값에 대한 타입을 표현할 수도 있다.

    여러 타입 연산자를 결합함으로써, 복잡한 연사과 값을 간결하고 유지보수 가능한 방식으로 표현할 수 있다.
    이 섹션에서는 기존 타입이나 값에 대한 새로운 타입을 표현하는 방법을 다룰 것이다.

    1. Generics - 매개변수를 가지는 타입들
      제네릭은 재사용 가능한 컴포넌트를 만드는 방법을 제공한다.
      제네릭 타입은 다른 타입과 연결되어 있으며, 특정 타입에 대해 작동하는 범용 타입으로 사용된다.

    2. Keyof Type Operator - keyof 연산자를 사용하여 새로운 타입 생성
