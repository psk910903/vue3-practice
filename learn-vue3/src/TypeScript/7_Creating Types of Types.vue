      이를 통해 객체의 키에 기반한 타입을 정의할 수 있다.

    3. Typeof Type Operator - typeof 연산자를 사용하여 새로운 타입 생성
      typeof 연산자는 변수나 속성의 타입을 캡처하여 사용한다.
      이를 통해 JavaScript 값의 타입을 기반으로 TypeScript 타입을 생성할 수 있다.

    4. Indexed Access Types - Type['a'] 구문을 사용한 타입의 하위 집합 접근
