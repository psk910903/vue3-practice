  app.config.optionMergeStrategies

    app.config.optionMergeStrategies는 Vue 3 애플리케이션에서 옵션 병합 전략을 정의하는 데 사용되는 객체이다.
    이를 통해 사용자 정의 옵션을 병합하는 방법을 정의할 수 있다.

    Vue 컴포넌트의 옵션은 전체 애플리케이션에서 상속될 수 있다. 예를 들어 'data', 'methods', 'computed' 등의 옵션이 컴포넌트 내에서 정의되어 있지 않으면
    상위 컴포넌트에서 해당 옵션을 상속받게 된다. 이러한 옵션의 병합은 사용자 정의 전략을 사용하여 제어할 수 있다.

    app.config.optionMergeStrategies 객체는 다음과 같은 형식을 가진다.

      {
        optionName: function (parentValue, childValue, vm) {
          // custom merge logic
        }
      }

    이 객체의 각 프로퍼티는 병합할 옵션의 이름을 나타내며, 해당 값은 두 개의 인수를 받는 함수이다.
    첫 번째 인수는 부모 컴포넌트에서의 옵션 값이고, 
    두 번째 인수는 자식 컴포넌트에서의 옵션 값이다.
    세 번째 인수는 Vue 인스턴스이다.

    이러한 함수 내에서 사용자 정의된 병합 로직을 구현할 수 있다.
    이 로직은 주로 어떤 옵션을 어떻게 병합할지를 결정하는 데 사용된다. 
    예를 들어, 두개의 객체를 합치거나 배열을 병합하는 등의 작업을 수행할 수 있다.

    옵션 병합 전략을 사용하면 Vue 컴포넌트의 동작을 더 세밀하게 제어할 수 있으며, 특정 상황에 필요한 병합 로직을 사용할 수 있다.
    그러나 보통은 기본 병합 전략을 사용하는 것이 일반적이며, 사용자 정의 옵션 병합은 특별한 상황에 필요한 경우에만 사용된다.


    컴포넌트 옵션의 커스텀 병합 함수를 정의하기 위한 객체이다.

    타입:
    
      interface AppConfig {
        optionMergeStrategies: Record<string, OptionMergeFunction>
      }

      type OptionMergeFunction = (to: unknown, from: unknown) => any

    세부사항
    
      일부 플러그인/라이브러리는 전역 믹스인(mixin)을 삽입하여, 커스텀 컴포넌트 옵션을 추가로 지원한다.
      여러 소스(예: 믹스인 또는 컴포넌트 상속)에서 동일한 옵션을 "병합"해야 하는 경우, 이러한 옵션에는 특별한 병합 로직이 필요할 수 있다.

      app.config.optionMergeStrategies 객체에 옵션 이름을 키(key)로 하는 커스텀 병합 함수를 할당하여 등록할 수 있다.

      병합 함수는 부모 및 자식 인스턴스에 정의된 해당 옵션의 값을 각각 첫 번째 그리고 두 번째 인자로 받는다.

    예제

      const app = createApp({
        // 자체 옵션
        msg: 'Vue',
        // 믹스인으로부터 전달받는 옵션
        mixins: [
          {
            msg: '안녕 '
          }
        ],
        mounted() {
          // this.$options에 병합된 옵션이 노출됨
          console.log(this.$options.msg)
        }
      })

      // `msg`의 커스텀 병합 로직 정의
      app.config.optionMergeStrategies.msg = (parent, child) => {
        return (parent || '') + (child || '')
      }

      app.mount('#app')
      // 앱 mounted 트리거 후 로그: '안녕 Vue'


