  app.config.errorHandler

    app.config.errorHandler는 Vue 애플리케이션에서 발생한 에러를 전역으로 처리하는 함수를 설정하는 속성이다.
    이 속성을 사용하면 애플리케이션 전체에서 발생한 예외를 캐치하고 처히라 수 있다.

    app.config.errorHandler는 일반적으로 개발 중에 발생하는 예외를 캐치하고 처리하는 데 사용된다.
    이를 통해 전역적으로 에러를 관리하고 사용자에게 피드백을 제공할 수 있다.
    예를 들어, 예외가 발생했을 때 콘솔에 로그를 출력하거나, 사용자에게 에러 메시지를 보여주는 등의 작업을 수행할 수 있다.

    이 함수는 다음과 같은 형식을 따른다.

      app.config.errorHandler = function (err, vm, info){
        // err : 발생한 에러 객체
        // vm  : Vue 인스턴스
        // info: Vue가 생성한 에러 정보(에러가 발생한 라이프사이클 훅과 관련 정보)

        //에러 처리 로직을 여기에 작성
      }

      이 함수는 세 개의 변수를 가지고 있다.

      1. err : 발생한 에러 객체이다. 이 객체에는 에러의 메시지, 스택 트레이스 등의 정보가 포함되어 있다.
      2. vm : 에러가 발생한 Vue 인스턴스이다. 이 인스턴스를 사용하여 애플리케이션의 상태를 확인하거나 조작할 수 있다.
      3. info : Vue가 생성한 에러 정보이다. 이 객체에는 에러가 발생한 라이프사이클 훅과 관련된 정보가 포함되어 있다.

      이 함수를 사용하여 발생한 예외를 적절히 처리하고 사용자에게 피드백을 제공할 수 있다.
      예를 들어, 발생한 예외를 콘솔에 로그로 출력하거나, 사용자에게 모달 창을 통해 에러 메시지를 보여주는 등의 작업을 수행할 수 있다.

    앱 내에서 예외 처리 되지 않은 에러 발생 시, 트리거 될 전역 핸들러를 정의힌다.

      * 스택 트레이스(stack trace)
        프로그램이 실행되는 동안 발생한 함수 호출의 순서를 역추적하거나 나타내는 정보.
        보통 예외가 발생했을 때 해당 예외가 발생한 원인을 찾는 데 사용된다.

        스택 트레이스는 함수 호출이 일어난 순서를 역순으로 기록한다. 가장 최근에 호출된 함수가 가장 위에 나타나고,
        호출 순서대로 아래로 내려간다. 스택 트레이스에는 각각의 함수 호출에 대한 정보가 포함되어 있으며, 일반적으로 함수의 이름, 파일명, 줄 번호 등이 표시된다.

        스택 트레이스는 주로 프로그램에서 발생한 오류를 디버깅할 때 사용된다. 예외가 발생했을 때 해당 예외가 발생한 위치를 파악하고,
        그 원인을 찾는 데 도움이 된다. 스택 트레이스를 통해 어떤 함수가 어디서 호출되었는지 추적할 수 있으며, 이를 통해 오류의 원인을 파악하고 수정할 수 있다.

        스택 트레이스는 주로 프로그래밍 언어나 개발 환경에 따라 다르게 표시될 수 있으며, 보통 에러 메시지나 디버깅 도구에서 제공된다.
        개발자 코드를 디버깅할 때 스택 트레이스를 자주 확인하여 문제의 원인을 파악하고 해결하는 데 활용한다.

    타입

      interface AppConfig {
        errorHandler?: (
          err: unknown,
          instance: ComponentPublicInstance | null,
          // `info`는 Vue 고유의 에러 정보.
          // 예: 에러 발생 생명 주기 훅 표시: 'created hook'
          info: string
        ) => void
      }

    세부 사항

      핸들러는 "에러", "에러를 트리거한 컴포넌트 인스턴스", "에러 출처 유형을 나타내는 문자열" 세 인자를 받는다.

      다음 출처에서 에러를 캡처할 수 있다.

      1. 컴포넌트 랜더
      2. 이벤트 핸들러
      3. 생명 주기 훅
      4. setup() 함수
      5. 감시자
      6. 커스텀 디렉티브 훅
      7. 트랜지션 훅

      TIP
        프로덕션에서는 3번째 인수 (info)가 전체 정보 문자열 대신 축약된 코드로 제공된다. 
        코드와 문자열 매핑은 프로덕션 오류 코드 참조(https://ko.vuejs.org/error-reference/#runtime-errors)에서 확인할 수 있다.

    예제

      app.config.errorHandler = (err, instance, info) => {
        // 에러 핸들링: 서비스 에러 로그 기록
      }

