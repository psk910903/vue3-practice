  app.config.performance

    app.config.performance는 Vue 애플리케이션의 성능 관련 설정을 담고 있는 속성이다.
    이 속성은 애플리케이션의 렌더링 및 감지 성능에 영향을 미치는 여러 가지 옵션을 구성할 수 있다.

    일반적으로 app.config.performance는 개발 중에 성능 모니터링 및 디버깅을 위해 사용된다.
    이를 통해 이플리케이션의 렌더링 시간, 업ㄷ에티으 시간 등을 측정하고 프로파일링할 수 있다.
    이 설정을 사용하여 개발 중에 성능 문제를 발견하고 해결할 수 있다.

    주요 성능 관련 옵션으로는 다음과 같은 것들이 있을 수 있다.

      1. app.config.performance.measure
        성능 측정을 활성하할지 여부를 결정한다. 기본값은 false이며, 이를 true로 설정하면 성능 측정이 활성화된다.
      2. app.config.performance.maxMemory
        렌더링 성능 모니터링 시 메모리 사용량의 최대 임계값을 설정한다. 이 값을 초과하는 경우 경고가 표시된다.
      3. app.config.performance.maxRenders
        렌더링 성능 모니터링 시 한 번의 업데이트에서 최대 렌더링 횟수를 설정한다. 이 값을 초과하는 경우 경고가 표시된다.

    이러한 성능 관련 설정을 사용하여 애플리케이션의 성능을 모니터링하고 최적화할 수 있다.
    그러나 일반적으로 이러한 설정은 개발 중에만 사용되며, 프로덕션 환경에서는 비활성화하는 것이 좋다.
    성능 측정은 애플리케이션의 성능에 영향을 미치며, 프로덕션 환경에서는 이러한 측정이 필요하지 않을 을 수 있다.

    
    이것을 true로 설정하면(크롬 기준) 브라우저 개발자 도구의 성능(Performance) 탭 -> 소요시간(Timings) 패널에 컴포넌트 초기화, 컴파일, 렌더링, 패치 성능 추적 기록이 활성화 된다.
    브라우저가 performance.mark API를 지원하고 개발 모드일 경우에만 작동(기록)한다.

    타입 : boolean

    1. app.config.compilerOptions.isCustomElement
      런타임 컴파일러 옵션을 설정한다. 이 객체에 설정된 값은 브라우저 내 템플릿 컴파일러에 전달되고,
      설정된 앱의 모든 컴포넌트에 영향을 미친다. compilerOptions 옵션을 사용하여 컴포넌트별로 이러한 옵션을 재정의할 수도 있다.

      중요
        이 옵션은 전체 빌드(예: 브라우저에서 템플릿을 컴파일할 수 있는 독립 실행성 vue.js)를 사용할 때만 적용된다.
        빌드 셋업과 함께 런타임 전용 빌드를 사용하는 경우, 빌드 도구 환경설정을 통해 컴파일러 옵션을 @vue/compiler-dom으로 전달해야 한다.
        - vue-loader의 경우: compilerOptions 로더 옵션을 통해 전달. vue-cli에서 환경설정 하는 방법도 참고 필요
        - vite의 경우 : @vitejs/plugin-vue 옵션을 통해 전달

      네이티브(native) 커스텀 엘리먼트 인식하기 위한 검사 방법을 지정한다.

      타입: (tag, string) -> boolean
      
      세부사항: 태그가 네이티브 커스텀 요소로 처리되어야 하는 경우, true가 반환돼야 한다. 일치하는 태그의 경우 Vue는 Vue 컴포넡트로 확인하는 대신 기본 엘리먼트로 렌더링한다.
                네이티브 HTML과 SVG 태그는 Vue 파서가 자동으로 인식하므로, 이 함수로 검사 작업을 할 필요가 없다.

      예제
        // 'ion-'으로 시작하는 모든 태그를 커스텀 엘리먼트로 처리
        app.config.compilerOptions.isCustomElement = (tag) => {
          return tag.startsWith('ion-')
        }

    2. app.config.compilerOptions.whitespace
      템플릿 내 공백 처리 방식을 설정한다.

      타입: 'condense' | 'preserve'

      기본 값: 'condense'

      세부사항: Vue는 템플릿에서 공백 문자를 제거/축소하여, 보다 효율적으로 컴파일된 결과물을 생성한다. 다음과 같이 기본적으로 "축소"에 초점을 두어 작동한다.
        1. 엘리먼트 내부의 선행/종료 공백 문자열은 단일 공백으로 축소된다.
        2. 엘리먼트 간의 개행(중바꿈)을 표함하는 공백 문자는 제거된다.
        3. 텍스트 노드의 연속된 공백 문자열은 단일 공백으로 축소된다.

      이 옵션을 'preserve'로 설정하면 (2)와 (3)이 비활성화된다.

      예제
        app.config.compilerOptions.whitespace = 'preserve'

    3. app.config.compilerOptions.delimiters
      템플릿 내 텍스트 보간에 사용되는 구분 기호를 설정한다.

      타입: [string, string]

      기본 값: ['{{','}}']

      세부 사항: 이것은 일반적으로 이중 중괄호 문법을 사용하는 서버 측 프레임워크와의 충돌을 피하기 위해 사용된다.

      예제:
        // 구분 기호가 ES6 템플릿 문자열 스타일로 변경됨
