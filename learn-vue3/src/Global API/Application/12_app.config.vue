  app.config

    app.config는 Vue 애플리케이션의 전역 설정을 담고 있는 객체이다. 
    이 객체를 사용하여 Vue 애플리케이션의 동작을 구성하고 조정할 수 있다.
    주요 구성 옵션들은 다음과 같다.

      1. app.config.productionTip
        개발 모드에서 Vue의 productionTip 메시지를 출력할지 여부를 결정한다. 기본값은 true이며 개발 중에는 true로 설정되어 있어서
        브라우저 콘솔에 Vue가 개발모드임을 알려주는 메시지가 출력된다. 프로덕션 모드에서는 보통 이 값을 false로 설정하여 이러한 메시지가 출력되지 않도록 한다.

      2. app.config.errorHandler
        애플리케이션에서 발생한 에러를 처리하는 함수를 설정한다. 이 함수는 전역적으로 애플리케이션에서 발생한 모든 에러를 캐치하고 처리할 수 있다.

      3. app.config.warnHandler
        Vue 경고 메시지를 처리하는 함수를 설정한다. 경고 메시지는 주로 개발 중에 잘못 사용된 Vue의 기능에 대한 경고로 사용된다.

      이 외에도 몇 가지 다른 구성 옵션이 있을 수 있으며, Vue의 향후 버전에서 변경될 수 있다.

      이러한 전역 설정은 Vue 애플리케이션의 동작을 조정하고 개발 및 디버깅 프로세스를 관리하는 데 도움이 된다.
      하지만 일반적으로 대부분의 애플리케이션에서는 이러한 설정을 변경할 필요가 없으며,
      기본값을 그대로 사용하는 것이 일반적이다. 필요한 경우에만 구성 옵션을 변경하여 특정한 동작을 조정할 수 있다.

