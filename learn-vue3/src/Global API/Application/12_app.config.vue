    app.config.globalProperties

      app.config.globalProperties는 Vue 3 애플리케이션에서 전역으로 사용자 정의 속성을 설정하는 데 사용되는 속성이다.
      이를 통해 모든 컴포넌트에서 사용할 수 있는 사용자 정의 속성을 정의할 수 있다.

      일반적으로 이러한 전역 속성은 Vue 인스턴스에 등록된 모든 컴포넌트에서 사용할 수 있다. 
      이는 특정 컴포넌트에 대해 반복적으로 속성을 정희할 필요 없이 전역으로 정의된 속성을 모든 컴포넌트에서 사용할 수 있다는 장점을 제공한다.

      예를 들어, 언어 설정과 같은 전역적인 설정 값을 사용하려는 경우
      app.config.globalProperties를 사용하여 이를 설정할 수 있다.

        import { createApp } from 'vue';
        import App from './App.vue';

        const app = createApp(App);

        app.config.globalProperties.$lang = 'en';

        app.mount('#app');

      위의 코드에서 $lang은 전역 속성으로 설정되었다. 이제 모든 Vue 컴포넌트에서 $lang을 사용하여 전역적인 언어 설정 값을 엑세스할 수 있다.

        <template>
          <div>
            <p>{{ $lang }}</p>
          </div>
        </template>

        <script>
        export default {
          mounted() {
            console.log(this.$lang); // 'en'
          }
        };
        </script>

      위의 예제에서 $lang은 모든 컴포넌트에서 사용 가능한 전역 속성으로 등록되어 있으며, 언어 설정과 같은 전역적인 데이터를 사용할 때 유용하게 사용될 수 있다.
      그러나 전역 속성은 남용되지 않도록 주의해야 한다. 과도한 전역 속성은 애플리케이션의 복잡성을 증가시킬 수 있으며, 컴포넌트 간의 의존성을 낮추는 것이 중요하다.


      앱 내부의 컴포넌트 인스턴스에 접근할 수 있는 전역 속성을 등록하는 데 사용되는 객체이다.

      타입

        interface AppConfig {
          globalProperties: Record<string, any>
        }

      세부 사항

        이것은 Vue 3에서 지원하지 않는 Vue 2의 Vue.prototype을 대체하며, 특별한 경우에만 사용해야 한다.
        전역 속성이 컴포넌트 자체 속성과 충돌하는 경우, 컴포넌트 자체 속성이 더 높은 우선 순위를 갖는다.

      사용법

        app.config.globalProperties.msg = '안녕!'

      이렇게 하면 앱 내 모든 컴포넌트 인스턴스의 this와 컴포넌트 템플릿에서 msg를 사용할 수 있다.

      export default {
        mounted() {
          console.log(this.msg) // '안녕!'
        }
      }

