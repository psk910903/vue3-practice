  app.config.warnHandler

    app.config.warnHandler는 Vue 애플리케이션에서 발생한 경고 메시지를 전역으로 처리하는 함수를 설정하는 속성이다.
    경고 메시지는 Vue가 개발 중에 발견한 잠재적인 문제를 알려주는 메시지로, 주로 잘못된 사용법이나 예상치 못한 동작에 대한 경고로 사용된다.

    app.config.warnHandler를 사용하면 전역적으로 발생한 경고 메시지를 캐치하고 처리할 수 있다.
    이를 통해 애플리케이션 전체에서 동일한 경고 처리 로직을 적용할 수 있다.

    이 함수는 다음과 같은 형식을 따른다.

      app.config.warnHandler = function (msg, vm, trace) {
        // msg : 발생한 경고 메시지
        // vm : Vue 인스턴스
        // trace : 경고 메시지의 스택 트레이스

        //경고 메시지 처리 로직을 여기에 작성한다.
      }
    
    이 함수는 세 개의 매개변수를 가지고 있다.

      1. msg : 발생한 경고 메시지다. 이 메시지에는 경고의 내용이 포함되어 있다.
      2. vm : 경고가 발생한 Vue 인스턴스이다. 이 인스턴스를 사용하여 애플리케이션의 상태를 확인하거나 조작할 수 있다.
      3. trace : 경고 메시지의 스택 트레이스이다. 이를 통해 경고가 발생한 위치와 관련된 정보를 얻을 수 있다.

    이 함수를 사용하여 발생한 경고를 적절히 처리하고 로깅하거나 사용자에게 피드백을 제공할 수 있다.
    예를 들어, 경고 메시지를 콘솔에 로그로 출력하거나, 사용자에게 알림을 표시하는 등의 작업을 수행할 수 있다.

    Vue에서 런타임 경고 발생 시, 트리거 될 커스텀 핸들러를 정의한다.

    타입

      interface AppConfig {
        warnHandler?: (
          msg: string,
          instance: ComponentPublicInstance | null,
          trace: string
        ) => void
      }

    세부 사항

      첫 번째 인자로 경고 메시지, 두 번째 인자로 발생한 컴포넌트 인스턴스, 세 번째 인자로 컴포넌트 추적 문자열을 받는다.

      특정 경고를 필터링하여 콘솔의 장황함을 줄일 수 있다. 모든 Vue 경고는 개발 중에 해결돼야 한다.
      따라서 이 함수는 디버깅 시에 많은 경고 중에서 특정 경고에 초점을 맞추는 것이 좋으며, 디버깅이 완료되면 제거해야 한다.

      TIP
        경고는 개발 모드에서만 발생하므로, 이것은 프로덕션 모드에서 무시된다.

      
    예제
      app.config.warnHandler = (msg, instance, trace) => {
        // `trace`는 컴포넌트 계층 구조를 추적한 문자열이다.
      }
      
